async def _retrieved_grace_ids_for_query(
    client: ArcadeClient, query_event_id: str
) -> list[str]:
    """Return the list of grace_ids for entities retrieved by a query event.

    Sorted by ``rank_ordinal`` ascending (tied ranks preserve write order).
    """
    escaped = escape_cypher_string(query_event_id)
    query = (
        f"MATCH (q:Query_Event {{query_event_id: '{escaped}'}})"
        f"-[r:retrieved_from]->(e) "
        f"RETURN e.grace_id AS grace_id, r.rank_ordinal AS rank_ordinal "
        f"ORDER BY r.rank_ordinal"
    )
    result = await client.execute_cypher(query)
    rows = result.get("result", []) or []
    grace_ids: list[str] = []
    for row in rows:
        if not isinstance(row, dict):
            continue
        gid = row.get("grace_id")
        if isinstance(gid, str) and gid:
            grace_ids.append(gid)
    return grace_ids


async def compare_retrieval_sets(
    client: ArcadeClient,
    query_event_id_a: str,
    query_event_id_b: str,
) -> dict[str, list[str]]:
    """Compare retrieval sets between two query events.

    Returns ``{"added", "removed", "unchanged"}`` where:

    * ``added``: grace_ids in B but not A.
    * ``removed``: grace_ids in A but not B.
    * ``unchanged``: grace_ids in both.

    Each list is deterministic in order: the diff lists follow the
    rank order of B (added) / A (removed) / A (unchanged).
    """
    a_ids = await _retrieved_grace_ids_for_query(client, query_event_id_a)
    b_ids = await _retrieved_grace_ids_for_query(client, query_event_id_b)
    a_set = set(a_ids)
    b_set = set(b_ids)
    added = [g for g in b_ids if g not in a_set]
    removed = [g for g in a_ids if g not in b_set]
    unchanged = [g for g in a_ids if g in b_set]
    return {"added": added, "removed": removed, "unchanged": unchanged}
